namespace Yuexintu.SDK.Model;

/// <summary>
/// 矩形结构体，表示位置和尺寸
/// </summary>
public struct Rect
{
	/// <summary>
	/// X坐标
	/// </summary>
	public int X { get; set; }
	/// <summary>
	/// Y坐标
	/// </summary>
	public int Y { get; set; }
	/// <summary>
	/// 宽度
	/// </summary>
	public int Width { get; set; }
	/// <summary>
	/// 高度
	/// </summary>
	public int Height { get; set; }
}